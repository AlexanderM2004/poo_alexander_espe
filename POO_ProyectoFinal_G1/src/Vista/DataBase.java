    private void Btt_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btt_DeleteActionPerformed
        try {
            int fila = Table_Motion.getSelectedRow();
            System.out.println(Table_Motion.getValueAt(fila, 0));
            String SQL = "delete from `motions` where codemov = " + Table_Motion.getValueAt(fila, 0);
            Statement st = (Statement) con.createStatement();
            int n = st.executeUpdate(SQL);
            if (n >= 0){
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
            }
            mostrarDatos();
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error al eliminar el registro, "+e);
        }
         
        if(Cmd_Estate.getSelectedItem() == "INGRESO"){
            int v1 = Integer.parseInt(txt_Monto.getText());
            monto = monto - v1;
            Lbl_Money.setText("$ "+ monto);
        }else{
            int v1 = Integer.parseInt(txt_Monto.getText());
            monto = monto + v1 ;
            Lbl_Money.setText("$ "+ monto);
        }
        
        mostrarDatos();
        
    }//GEN-LAST:event_Btt_DeleteActionPerformed

    private void Btt_Add1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btt_Add1ActionPerformed
        String SQL = "insert into motions(date, cash, activity,description)values(?,?,?,?)";
        try {
            PreparedStatement pst = (PreparedStatement) con.prepareStatement(SQL);
            int select = Cmd_Estate.getSelectedIndex();
            pst.setString(1,txt_Date.getText());
            pst.setString(2,txt_Monto.getText());
            pst.setString(3,Cmd_Estate.getItemAt(select));
            pst.setString(4,txt_Description.getText());
            pst.execute();
        } catch(Exception e){
            System.out.println(e);
        }

        //Contador
        if(Cmd_Estate.getSelectedItem() == "INGRESO"){
            int v1 = Integer.parseInt(txt_Monto.getText());
            monto = v1 + monto;
            Lbl_Money.setText("$ "+ monto);
        }else{
            int v1 = Integer.parseInt(txt_Monto.getText());
            monto = monto - v1 ;
            Lbl_Money.setText("$ "+ monto);
        }
        mostrarDatos(); 
    }//GEN-LAST:event_Btt_Add1ActionPerformed

