    public void mostrarDatos(){
        String titulos[] = {"ID", "Fecha", "Monto", "Actividad", "Descripci√≥n"};
        String registro[] = new String[5];
        DefaultTableModel modelo = new DefaultTableModel(null, titulos);
        String SQL = "SELECT * FROM motions";
        try {
            Statement st = (Statement) con.createStatement();
            ResultSet rs = st.executeQuery(SQL);
            while(rs.next()){
                registro[0] = rs.getString("codemov");
                registro[1] = rs.getString("date");
                registro[2] = rs.getString("cash");
                registro[3] = rs.getString("activity");
                registro[4] = rs.getString("description");
                modelo.addRow(registro);
            }
            Table_Motion.setModel(modelo);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error al mostrar los datos. "+e);
            System.out.println(e);
        }  
    }
    
