//Operadores
package poo_practica2.operadores._alexanderq;
public class POO_Practica2Operadores_AlexanderQ {
    public static void main(String[] args) {
        int a = 10;
        int b = 4;
        int suma,resta,multi,divi;
        System.out.println("--------------------------------------------");
        System.out.print("Primer numero: ");
        System.out.println(a);
        System.out.print("Segundo numero: ");
        System.out.println(b);
        System.out.println("--------------------------------------------");
        System.out.print("La suma de los numeros es: ");
        suma=a+b;
        System.out.println(suma);
        System.out.println("--------------------------------------------");
        System.out.print("La resta de los numeros es: ");
        resta=a-b;
        System.out.println(resta);
        System.out.println("--------------------------------------------");
        System.out.print("La mutiplicacion de los numeros es: ");
        multi=a*b;
        System.out.println(multi);
        System.out.println("--------------------------------------------");
        System.out.print("La divicion de los numeros es: ");
        divi=a/b;
        System.out.println(divi);
        System.out.println("--------------------------------------------");
        System.out.print("Primer numero aumentado 1: ");
        a++;
        System.out.println(a);
        System.out.print("Segundo numero aumentado 1: ");
        b++;
        System.out.println(b);
    }
    
}
