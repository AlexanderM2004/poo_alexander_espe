//Operadores Logicos
package poo_practica4.ologicos._alexanderq;
public class POO_Practica4OLogicos_AlexanderQ {
    public static void main(String[] args) {
        int val1 = 7, val2 = 2;
        System.out.println((val1 == val2)&&(val1 <= val2)); //Operqador logico AND
        System.out.println((val1 >= val2)||(val1 != val2)); //Operador logico OR
        System.out.println(val1 != val2); //Operador logico NOT
    }
    
}
