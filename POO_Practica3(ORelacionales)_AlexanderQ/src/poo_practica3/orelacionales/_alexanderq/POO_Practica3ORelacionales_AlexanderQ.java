//Operadores Relacionales
package poo_practica3.orelacionales._alexanderq;
public class POO_Practica3ORelacionales_AlexanderQ {
    public static void main(String[] args) {
        int val1=5;
        int val2=1;
        System.out.println(val1 == val2);
        System.out.println(val1 != val2);
        System.out.println(val1 > val2);
        System.out.println(val1 < val2);
        System.out.println(val1 >= val2);
        System.out.println(val1 <= val2);
    }
    
}
